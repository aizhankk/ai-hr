@asynccontextmanager
async def get_db_connection_by_token(request: Request):
    if db_pool is None:
        raise ServiceError(status_id=0)  # TODO: set the required status_id

    auth_header = request.headers.get("Authorization")
    if not auth_header:
        raise HTTPException(status_code=401, detail="Missing Authorization header")
    try:
        scheme, token = auth_header.split()
        if scheme.lower() != "bearer":
            raise HTTPException(
                status_code=401, detail="Invalid authentication scheme"
            )
    except ValueError:
        raise HTTPException(
            status_code=401, detail="Invalid Authorization header format"
        )

    try:
        # Token decoding without signature verification.
        # Do not use this approach in production unless intentional.
        payload = jwt.decode(token, options={"verify_signature": False})
    except Exception:
        raise HTTPException(status_code=401, detail="Invalid token")

    user_id = payload.get("subject_id")
    if not user_id:
        raise HTTPException(status_code=401, detail="Token missing subject")

    async with db_pool.acquire() as conn:
        await conn.execute(f"SET my.current_user_id = '{user_id}';")
        yield conn, user_id


@asynccontextmanager
async def use_or_create_db(db=None):
    if db is None:
        log.info("Creating new database connection.")
        async with get_db_connection() as new_db:
            async with new_db.transaction():
                yield new_db
        return
    yield db


def get_db_conn(request: Request):
    """Extract connection from request.state set by middleware."""
    conn = getattr(request.state, "db", None)
    if conn is None:
        raise HTTPException(status_code=500, detail="DB connection not found")
    return conn
